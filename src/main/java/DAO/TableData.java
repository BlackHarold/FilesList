package DAO;

public class TableData {
    
    public int number;
    public String name;
    public String location;
    public Double size;
    
    public TableData() {
    
    }
    
    public TableData(int number, String name, String location, Double size) {
        this.number = number;
        this.name = name;
        this.location = location;
        this.size = size;
    }
}
